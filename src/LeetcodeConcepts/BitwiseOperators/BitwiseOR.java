package LeetcodeConcepts.BitwiseOperators;

    public class BitwiseOR {
        public static void main(String[] args) {
            int a = 5;  // 0101
            int b = 3;  // 0011
            int result = a | b; // 0111 (7)

            System.out.println("Result of 5 | 3: " + result);
        }
    }

